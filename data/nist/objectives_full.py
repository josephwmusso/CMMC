def validate_objectives():
    """Validate the objectives dataset."""
    print(f"Total objectives: {len(ASSESSMENT_OBJECTIVES)}")

    # Check unique IDs
    ids = [o["id"] for o in ASSESSMENT_OBJECTIVES]
    dupes = [x for x in ids if ids.count(x) > 1]
    if dupes:
        print(f"WARNING: Duplicate objective IDs: {set(dupes)}")
    else:
        print("All objective IDs unique")

    # Count per control
    from collections import Counter
    control_counts = Counter(o["control_id"] for o in ASSESSMENT_OBJECTIVES)
    print(f"Controls with objectives: {len(control_counts)}")
    print(f"Min objectives per control: {min(control_counts.values())}")
    print(f"Max objectives per control: {max(control_counts.values())}")
    print(f"Avg objectives per control: {sum(control_counts.values()) / len(control_counts):.1f}")

    # Check all EIT fields present
    missing = [o["id"] for o in ASSESSMENT_OBJECTIVES
               if not o.get("examine") or not o.get("interview") or not o.get("test")]
    if missing:
        print(f"WARNING: {len(missing)} objectives missing EIT data")
    else:
        print("All objectives have Examine/Interview/Test data")

    print("\nValidation complete!")


if __name__ == "__main__":
    validate_objectives()
