// Helper function to get controls by family
export function getControlsByFamily(familyId: string): Control[] {
  return controls.filter(c => c.family === familyId);
}

// Helper function to get control statistics
export function getControlStats() {
  const total = controls.length;
  const implemented = controls.filter(c => c.status === 'Implemented').length;
  const partial = controls.filter(c => c.status === 'Partially Implemented').length;
  const planned = controls.filter(c => c.status === 'Planned').length;
  const notImplemented = controls.filter(c => c.status === 'Not Implemented').length;

  return {
    total,
    implemented,
    partial,
    planned,
    notImplemented,
    percentComplete: Math.round((implemented / total) * 100),
  };
}